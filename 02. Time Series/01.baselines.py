def evaluate_baselines(test_loader, variant, num_features):
    target_idx = 1 if num_features == 3 else 0
    ys_true = []
    ys_pred = []

    for x, y in test_loader:
        # x shape is (batch, W, features)
        # y shape is (batch,)
        ys_true.append(y.numpy())
        if variant == "naive":
            # Predict y_t+1 as the last step in the history window: x[:, -1, target_idx]
            pred = x[:, -1, target_idx].numpy()
        elif variant == "ma":
            # Predict y_t+1 as the average of the history window: mean(x[:, :, target_idx])
            pred = x[:, :, target_idx].mean(dim=1).numpy()
        else:
            raise ValueError(f"Unknown variant: {variant}")
        ys_pred.append(pred)

    return np.concatenate(ys_true), np.concatenate(ys_pred)


def run_arima(train_loader, test_loader, num_features, limit):
    """Fits an ARIMA model on a subset of the training data and forecasts step-by-step."""
    target_idx = 1 if num_features == 3 else 0

    # Collect all train targets to fit ARIMA
    train_targets = []
    for _, y in train_loader:
        train_targets.append(y.numpy())
    train_seq = np.concatenate(train_targets)

    # Collect test targets
    test_targets = []
    for _, y in test_loader:
        test_targets.append(y.numpy())
    test_seq = np.concatenate(test_targets)

    # Limit train_seq size for ARIMA to prevent infinite run times in demos
    fit_len = min(1000, len(train_seq))
    train_seq = train_seq[-fit_len:]

    try:
        from statsmodels.tsa.arima.model import ARIMA
        print(f"Fitting ARIMA(2,1,0) on {len(train_seq)} points...")
        # Fit on training data
        model = ARIMA(train_seq, order=(2, 1, 0))
        model_fit = model.fit()

        # Step-by-step forecast on test data (rolling forecast or simple prediction)
        # To keep it fast, we can use the model to predict the next steps.
        # A simple one-step-ahead forecasting without manual retraining for every step:
        # We can use out-of-sample forecast or rolling prediction.
        # For simplicity and speed, we perform out-of-sample prediction using statsmodels.
        predictions = model_fit.forecast(steps=len(test_seq))
    except Exception as e:
        print(f"statsmodels ARIMA failed or not installed: {e}")
        print("Falling back to a simple Autoregressive baseline...")
        # Fallback: Simple AR(2) model fitted using Least Squares
        # y_t = c + phi1 * y_t-1 + phi2 * y_t-2
        X_ar, y_ar = [], []
        for i in range(2, len(train_seq)):
            X_ar.append([train_seq[i-1], train_seq[i-2]])
            y_ar.append(train_seq[i])
        X_ar = np.array(X_ar)
        y_ar = np.array(y_ar)
        # Solve least squares: w = (X^T X)^-1 X^T y
        w = np.linalg.pinv(X_ar.T @ X_ar) @ X_ar.T @ y_ar
        # Predict on test
        predictions = []
        # We use a rolling window of history to predict next step
        history = list(train_seq[-2:])
        for target in test_seq:
            pred = w[0] * history[-1] + w[1] * history[-2]
            predictions.append(pred)
            history.append(target)
        predictions = np.array(predictions)

    return test_seq, predictions


def main():
    p = mc.build_argparser("Classical Baselines for Time Series")
    # Override variant choices
    p.add_argument("--baseline-variant", type=str, default="naive", choices=["naive", "ma", "arima"],
                   help="override variant parameter")
    args = p.parse_args()

    # Determine variant
    variant = args.baseline_variant if args.baseline_variant != "naive" else (args.variant or "naive")
    if variant not in ["naive", "ma", "arima"]:
        variant = "naive"

    train_loader, test_loader, num_features, mean, std = mc.get_dataloaders(
        args.dataset, args.batch_size, args.limit
    )

    print(f"Running Baseline Variant: {variant.upper()} on {args.dataset.upper()} dataset")

    if variant in ["naive", "ma"]:
        y_true, y_pred = evaluate_baselines(test_loader, variant, num_features)
    elif variant == "arima":
        y_true, y_pred = run_arima(train_loader, test_loader, num_features, args.limit)

    mc.report(y_true, y_pred, mean, std, dataset_name=args.dataset,
              model_name=f"Baseline-{variant.upper()}",
              save_dir=None if args.no_figure else os.path.dirname(os.path.abspath(__file__)))


if __name__ == "__main__":
    main()
