"""
01. Classical Baselines for Time Series Forecasting
===================================================

Non-deep learning statistical baselines. These serve as the baseline benchmarks
for predicting time-series values.

Methods:
    * Naive / Persistence: Predicts the next step will be equal to the current step (y_t+1 = y_t).
    * Moving Average: Predicts the average of the last N historical steps.
    * ARIMA (Autoregressive Integrated Moving Average): Standard parametric statistical forecasting.

Run:
    python "01.baselines.py" --dataset jena --variant naive
    python "01.baselines.py" --dataset spy --variant ma
"""

